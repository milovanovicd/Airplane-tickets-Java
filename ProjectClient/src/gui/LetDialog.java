    private void btnSačuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSačuvajActionPerformed
        try {
            Destinacija polaznaDestinacija = (Destinacija) cmbPolaznaDestinacija.getSelectedItem();
            Destinacija krajnjaDestinacija = (Destinacija) cmbKrajnjaDestinacija.getSelectedItem();
            Date datumPolaska = new SimpleDateFormat("dd/MM/yyyy").parse(txtDatumPolaska.getText());
            Date datumPovratka = new SimpleDateFormat("dd/MM/yyyy").parse(txtDatumPovratka.getText());
            int duzinaLeta = Integer.parseInt(txtDuzinaLeta.getText());
            BigDecimal cenaLeta = new BigDecimal(txtCenaLeta.getText());
            Avion avion = (Avion) cmbAvion.getSelectedItem();

            Let l = new Let(-1, datumPolaska, datumPovratka, duzinaLeta, cenaLeta, polaznaDestinacija, krajnjaDestinacija, avion);

            if (status.equals("insert")) {
                l = KomunikacijaSaServerom.getInstanca().dodajLet(l);
                if (l.getLetID() != -1) {
                    JOptionPane.showMessageDialog(this, "Let je uspešno unet!","Unos leta",JOptionPane.INFORMATION_MESSAGE);
                    parent.osvezi();
//                    parent.setEnabled(true);
                    dispose();
                }
            } else {
                long idLeta = let.getLetID();
                l.setLetID(idLeta);
                l = KomunikacijaSaServerom.getInstanca().izmeniLet(l);
                parent.osvezi();
                JOptionPane.showMessageDialog(this, "Sistem je izmenio let!","Izmena leta",JOptionPane.INFORMATION_MESSAGE);
//                parent.setEnabled(true);
                dispose();
            }
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Numerička polja ne smeju biti prazna!","Let dialog",JOptionPane.ERROR_MESSAGE);
        }catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Neispravan format datuma!","Let dialog",JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Greška prilikom popunjavanja polja!","Let dialog",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(LetDialog.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Let dialog",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(LetDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSačuvajActionPerformed

