    private void fillFields() throws IOException, Exception  {
        LinkedList<DomainObject> destinacije = KomunikacijaSaServerom.getInstanca().vratiDestinacije();
        LinkedList<DomainObject> avioprevoznici = KomunikacijaSaServerom.getInstanca().vratiAvioprevoznike();
        this.avioni = KomunikacijaSaServerom.getInstanca().vratiAvione();
        cmbPolaznaDestinacija.removeAllItems();
        cmbKrajnjaDestinacija.removeAllItems();
        cmbAvioprevoznik.removeAllItems();
        cmbAvion.removeAllItems();

        for (DomainObject polaznaDestinacija : destinacije) {
            cmbPolaznaDestinacija.addItem(polaznaDestinacija);
        }
        
        for (DomainObject krajnjaDestinacija : destinacije) {
            cmbKrajnjaDestinacija.addItem(krajnjaDestinacija);
        }
        for (DomainObject avioprevoznik : avioprevoznici) {
            cmbAvioprevoznik.addItem(avioprevoznik);
        }

        for (DomainObject avion : avioni) {
            Avion domain = (Avion) avion;
            if (domain.getAvioprevoznik().equals(avioprevoznici.getFirst())) {
                cmbAvion.addItem(domain);
            }

        }

    }

    private void fillFieldsForUpdate() throws Exception {
        fillFields();
        
        this.let = KomunikacijaSaServerom.getInstanca().vratiLet(let);
        cmbPolaznaDestinacija.setSelectedItem(let.getLetOd());
        cmbKrajnjaDestinacija.setSelectedItem(let.getLetDo());
        cmbAvioprevoznik.setSelectedItem(let.getAvion().getAvioprevoznik());

        //Dodavanje odgovarajuće liste aviona u zavisnosti od avioprevoznika i setovanje Aviona
        cmbAvion.removeAllItems();
        for (DomainObject avion : avioni) {
            Avion domain = (Avion) avion;
            if (domain.getAvioprevoznik().equals(let.getAvion().getAvioprevoznik())) {
                cmbAvion.addItem(domain);
            }

        }

        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        txtDatumPolaska.setText(format.format(let.getDatumPolaska()));
        txtDatumPovratka.setText(format.format(let.getDatumPovratka()));
        txtDuzinaLeta.setText(let.getDuzinaLeta() + "");
        txtCenaLeta.setText(let.getCenaLeta() + "");

    }

