    private void btnRezervisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRezervisiActionPerformed
        try {
            Date datumRezervacije = new SimpleDateFormat("dd/MM/yyyy").parse(txtDatumRezervacije.getText());
            Rezervacija r = new Rezervacija(let, korisnik, datumRezervacije, false);

            r = KomunikacijaSaServerom.getInstanca().dodajRezervaciju(r);
            
            JOptionPane.showMessageDialog(this, "Sistem je kreirao rezervaciju!","Kreiranje rezervacije",JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (ParseException ex) {
            Logger.getLogger(RezervacijaDialog.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(RezervacijaDialog.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne može da kreira rezervaciju! Rezervacija već postoji!","Kreiranje rezervacije",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(RezervacijaDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRezervisiActionPerformed

