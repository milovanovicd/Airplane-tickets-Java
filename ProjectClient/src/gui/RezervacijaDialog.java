    private void fillForm() throws Exception {
        this.let = KomunikacijaSaServerom.getInstanca().vratiLet(let);

        txtLet.setText(let.toString());
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        txtDatumPolaska.setText(format.format(let.getDatumPolaska()));
        txtDatumRezervacije.setText(format.format(new Date()));
        txtImePrezime.setText(korisnik.getIme() + " " + korisnik.getPrezime());
        txtBrojPasosa.setText(korisnik.getBrPasosa() + "");
        txtCena.setText(let.getCenaLeta() + " RSD");
    }
