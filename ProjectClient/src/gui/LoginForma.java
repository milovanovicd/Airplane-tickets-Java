    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtLoginUsername.getText();
        String password = String.copyValueOf(txtLoginPassword.getPassword());
        Korisnik k = new Korisnik();
        k.setUsername(username);
        k.setPassword(password);

        try {
            k = KomunikacijaSaServerom.getInstanca().login(k);
            if (k != null && k.getId() != -1) {
                JOptionPane.showMessageDialog(this, "Uspe≈°na prijava na sistem","Prijava na sistem",JOptionPane.INFORMATION_MESSAGE);
                if (k.isAdmin()) {
                    JFrame jf = new GlavnaAdminForma(k);
                    jf.setVisible(true);
                } else {
                    JFrame jf = new GlavnaKlijentskaForma(k);
                    jf.setVisible(true);
                }
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Neuspe≈°na prijava na sistem","Prijava na sistem",JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Prijava na sistem",JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(),"Prijava na sistem",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        try {
            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            long brPasosa = Long.parseLong(txtBrPasosa.getText());
            String brTelefona = txtBrTelefona.getText();
            String username = txtUsername.getText();
            String password = String.copyValueOf(txtPassword.getPassword());
            boolean admin = false;

            Korisnik k = new Korisnik(-1, brPasosa, ime, prezime, brTelefona, username, password, admin);
            try {
                k = KomunikacijaSaServerom.getInstanca().register(k);
                if (k != null && k.getId() != -1) {
                    JOptionPane.showMessageDialog(this, "Registracija uspe≈°na! Mo≈æete se ulogovati sa va≈°im kredencijalima!","Registracija",JOptionPane.INFORMATION_MESSAGE);
                    RegisterDialog.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Registracija neuspe≈°na!","Registracija",JOptionPane.ERROR_MESSAGE);
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(),"Registracija",JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
//            Logger.getLogger(LoginForma.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, ex.getMessage(),"Registracija",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            //Mo≈æe mo≈æda bolje da se implementira
            JOptionPane.showMessageDialog(this, "Polje 'Broj paso≈°a' nije u odgovarajuƒáem formatu ili je prazno!","Registracija",JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnConfirmActionPerformed


